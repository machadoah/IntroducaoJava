package V_VariablesAndDataTypes;

public class III_IncrementAndDecrement {

    public static void main(String[] args) {

//          Pos-Increment

        int a = 9;
        a++;
        System.out.println(a); // a <- a + 1, prints 10

        int c = 66;
        c += 2; // equal to c = c + 2
        System.out.println(c); // c <- c + 2, prints 68

        // also is possible to utilize others arithmetic operators, as +,-,* e /

        int j = 100;
        j /= 2; // j = 100/2 so j <- 50
        System.out.println(j);

//          Decrement

        int p = 5;
        p--; // equal to p = p - 1
        System.out.println(p); // p <- p - 1, prints 4


    }
}
